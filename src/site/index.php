<h1>What is JetriX ?</h1>

JetriX is a new generation TetriNET server written in Java and designed for 
maximum scalability and extensibility. It features a pluggable filter and 
command API allowing easy channel behavior customization that requires little 
knowledge of the server's inner functioning. The ambitious goal of this project
is to create the ideal plateform for server side TetriNET programmers. JetriX 
is open source and developped under the <a href="license.php">GNU General 
Public License</a>.
