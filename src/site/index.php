<h1>What is JetriX ?</h1>

JetriX is a new generation TetriNET server written in Java and designed for maximum scalability and extensibility. It features a pluggable filter and command API allowing easy channel behavior customization that requires little knowledge of the server's inner functioning. The ambitious goal of this project is to create the ideal plateform for server side TetriNET programmers. JetriX is open source and developped under the GPL.
<br /><br />

<h1>Features planned for JetriX 1.0</h1>
 
<ul>
  <li> multi channel</li>
  <li> multi client : tetrinet &amp; tetrifast</li>
  <li> pluggable and extensible filter system</li>
  <li> pluggable command system</li>
  <li> player authentication</li>
  <li> game recording &amp; playback</li>
  <li> interface to external ladder systems</li>
  <li> TSpec support</li>
  <li> IRC support</li>
  <li> Query support</li>
  <li> default filters : anti-flood, badwords, ppm counter...</li>
  <li> internationalization</li>
  <li> XML based configuration files</li>
  <li> web based administration</li>
  <li> interactive console administration</li>
  <li> hot reconfiguration</li>
  <li> auto updater</li>
  <li> IPv6 support</li>
</ul>

