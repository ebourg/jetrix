<h1>Road Map</h1>

This page summarizes the developpement plan for the next releases. The first 
goal is to match the features of tetrinetx and then add the cool features up 
to the version 1.0.

<h2>JetriX 0.1.2</h2>

<ul>
  <li>Basic TSpec support</li>
  <li>Basic web administration (read only)</li>
  <li>Winlists</li>
  <li><tt>/move</tt> command</li>
</ul>

<h2>JetriX 0.1.3</h2>

<ul>
  <li>Full TSpec support</li>
  <li>Ban list</li>
  <li>PPM</li>
</ul>

<h2>JetriX 0.1.4</h2>

<ul>
  <li>Query protocol support</li>
  <li>IRC Clients support</li>  
</ul>

<h2>JetriX 0.1.5</h2>

<ul>
  <li>Dynamic channel creation</li>
  <li>Full web administration</li>
</ul>

<h2>JetriX 0.2</h2>

<ul>
  <li>Feature equivalent to tetrinetx</li>
  <li>tetrinetx to Jetrix migration tool</li>
  <li>Database integration</li>
  <li>Player authentication</li>
</ul>


